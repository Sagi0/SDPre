public class Objective7Lab4 {
  public static void main(String[] args) {
    int counter = 0;
    int sum = 0;
    int count;

    while(counter<=20) {
      count = counter;
      sum = sum + count;
      counter = counter + 1;

    if(sum>=210) {
      System.out.println(sum);
      
      }
    }
  }
}
